                            	<h3>Course Learning Outcomes</h3>
                                <p><strong>Knowledge and understanding</strong></p>
                                                        <p>Students will be expected to gain the following knowledge during the Course of study:</p>
                                                        <ul>
                                                        	<li>Developing the knowledge, understanding and skills of organisations, the business environment in which they operate and their management. </li>
                                                            <li>Demonstrating knowledge and understanding Markets, and Marketing and sales, the management of resources including the supply chain, procurement, logistics, and outsourcing.  </li>
                                                            <li>Equipping students with awareness of Customer management and relationship and leadership. </li>
                                                            <li>Developing knowledge of different financial sources and the use of accounting and managing financial risk. </li>
                                                            <li>Understanding the use of relevant communication in business and management including the use of digital technology. </li>
                                                            <li>Developing appropriate policies and strategies within a changing environment to meet stakeholders’ interest and the use of risk management techniques. </li>
                                                            <li>Providing innovative business ideas to create new products, services or organisations. </li>
                                                            <li>Realising the need for individuals and organisations to manage responsibility and behave ethically in relation to social, cultural, economic and environmental issues. </li>
                                                        </ul>                                               
                                                    
