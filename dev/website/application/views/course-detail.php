                            	<h3>Teaching and Learning</h3>
                                <p>The College recognises that its Teaching, Learning and Assessment Strategy is fundamental to achieving the aims set out in its Mission Statement and to satisfy expectations contained in appropriate indicators in Chapter B3, B4 and B6 of the UK Quality Code for the Assurance of Academic Quality and Standards in Higher Education. </p>
                                                        <p>The aims of the Teaching, Learning and Assessment Strategy is to achieve the following: </p>
                                                        <ul>
                                                        	<li>To widen participation from students who are mature, from Black and Minority Ethnic Communities, and come from lower socio-economic backgrounds </li>
                                                            <li>To educate students who are motivated and self-directed critical thinkers, capable of independent enquiry </li>
                                                            <li>To provide students with both sound academic knowledge and vocational expertise </li>
                                                            <li>To foster independent and collaborative learning among students and to encourage lifelong learning leading to enhancing their career potentials </li>
                                                            <li>To develop and implement approaches to feedback and assessment that maximise learning and student outcomes. </li>
                                                        </ul>
                                                        <p>(For more details please see The College Quality and Enhancement Manual) </p>
