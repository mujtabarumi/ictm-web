                        <div class="tab-pane fade" id="tab7danger">
                        	<div class="course-detail-section">
                            	<h3>Assessment and Feedback</h3>
                                <p>The College adheres to the adopted assessment policies and procedures that are published in the Quality and Enhancement Manual (QAEM) which is in line with the UK Quality Code. Effective assessment rests with the purpose for which the assessment is carried out as well as the nature and type of appropriate assessment tools used. In essence the assessment materials and tools should be fit-for-purpose. The college assessor and internal verifier assured that assignment brief of the assignments are fair and accurate as much as possible.</p>
                                                        <p>As required by Pearson, according to the Course specifications, the key assessment objectives and strategies are aimed at assessing the achievement of a number of specific learning outcomes in every unit against specific assessment criteria.</p>
                                                        <p>The College uses both formal and informal assessment strategies. The College uses a variety of assessment methods to enhance learning and improves the validity of assessment. The assessment methods improve the knowledge of the assessment criteria and what is required to gain higher grade achievement. There is a range of assessment methods that are  utilised, such as: presentations; written reports.  As an informal assessment strategy, the College implements a formative method of assessment which requires students to submit ‘task by task’ coursework during the semester.</p>
                                                        <p>This Course is assessed using a combination of ICON College and Pearson-set assignments.  Each year, Pearson will issue a Theme and (for Level 4) a set of related Topics. ICON College will develop an assignment, to be internally assessed, to engage students in work related to the Pearson-set Theme.</p>
                                                        <p>At Level 4, students will select a Topic to further define their approach to the Theme and assignment. At Level 5, it is expected that students will define their own Topic, in negotiation with Tutors, based on the Pearson-set Theme.</p>
